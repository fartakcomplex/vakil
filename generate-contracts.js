function esc(str) {
  return str.replace(/'/g, "\\'").replace(/\n/g, ' ');
}

function generateContract(title, slug, description, summary, categorySlug, tags, applicableLaws, difficulty, content) {
  return `  {
    title: '${esc(title)}',
    slug: '${esc(slug)}',
    description: '${esc(description)}',
    content: \`${content}\`,
    summary: '${esc(summary)}',
    categorySlug: '${esc(categorySlug)}',
    tags: [${tags.map(t => `'${esc(t)}'`).join(', ')}],
    applicableLaws: [${applicableLaws.map(l => `'${esc(l)}'`).join(', ')}],
    difficulty: '${difficulty}',
  }`;
}

