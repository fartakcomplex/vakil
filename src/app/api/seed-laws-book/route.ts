function lawHTML(chapters: { title: string; articles: { num: number; title: string; text: string }[] }[]): string {
  let html = `<div dir="rtl" style="font-family: Vazirmatn, sans-serif; line-height: 2.2;">\n`;
  for (const ch of chapters) {
    html += `  <h2 style="color: #1e40af; border-bottom: 2px solid #1e40af; padding-bottom: 8px; margin-top: 24px;">${ch.title}</h2>\n`;
    for (const a of ch.articles) {
      html += `  <h3 style="color: #374151; margin-top: 16px;">ماده ${a.num}${a.title ? ' - ' + a.title : ''}</h3>\n`;
      html += `  <p style="text-align: justify; color: #1f2937; margin: 8px 0;">${a.text}</p>\n`;
    }
  }
  html += `</div>`;
  return html;
}

