// ============ POST HANDLER ============
export async function POST() {
  try {
    // Speed up SQLite
    try { await db.$executeRaw('PRAGMA synchronous = OFF'); } catch { /* ignore */ }
    try { await db.$executeRaw('PRAGMA journal_mode = WAL'); } catch { /* ignore */ }

    // Clear existing
    await db.legalLaw.deleteMany();
    await db.legalLawCategory.deleteMany();

    // Create categories
    const catMap = new Map<string, string>();
    for (const c of CATS) {
      const cat = await db.legalLawCategory.create({
        data: { name: c.name, slug: c.slug, description: c.desc, icon: c.icon, color: c.color, order: c.order },
      });
      catMap.set(c.slug, cat.id);
    }

    // Create laws
    let created = 0;
    for (const law of LAWS) {
      const catId = catMap.get(law.cat);
      if (!catId) continue;

      await db.legalLaw.create({
        data: {
          title: law.title,
          slug: law.slug,
          description: law.desc,
          summary: law.summary,
          content: lawHTML(law.chapters),
          categoryId: catId,
          lawNumber: law.lawNumber,
          lawType: law.lawType,
          approvalDate: law.approvalDate,
          source: law.source,
          tags: JSON.stringify(law.tags),
          difficulty: law.difficulty,
          isPublished: true,
          viewCount: Math.floor(Math.random() * 800) + 100,
          version: '2.0',
        },
      });
      created++;
    }

    return NextResponse.json({
      success: true,
      message: `کتاب قوانین ایران با موفقیت ایجاد شد`,
      categories: CATS.length,
      laws: created,
    });
  } catch (error) {
    console.error('Seed laws book error:', error);
    return NextResponse.json(
      { success: false, error: 'خطا در ایجاد کتاب قوانین' },
      { status: 500 }
    );
  }
}
