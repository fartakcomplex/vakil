export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { messages, mode = 'general' } = body;

    if (!messages || !Array.isArray(messages) || messages.length === 0) {
      return NextResponse.json(
        { error: 'پیام‌های مورد نیاز ارسال نشده است' },
        { status: 400 }
      );
    }

    // Use z-ai-web-dev-sdk
    const ZAI = (await import('z-ai-web-dev-sdk')).default;
    const zai = await ZAI.create();

    const systemMessage = SYSTEM_PROMPT + (MODE_PROMPTS[mode] || MODE_PROMPTS.general);

    const chatMessages = [
      { role: 'system', content: systemMessage },
      ...messages.map((m: { role: string; content: string }) => ({
        role: m.role,
        content: m.content,
      })),
    ];

    const completion = await zai.chat.completions.create({
      messages: chatMessages,
      temperature: 0.7,
      max_tokens: 2000,
    });

    const aiMessage = completion.choices?.[0]?.message?.content || 'پاسخی دریافت نشد. لطفاً دوباره تلاش کنید.';

    return NextResponse.json({ message: aiMessage });
  } catch (error) {
    console.error('AI API Error:', error);

    // Return fallback response on error
    try {
      const body = await request.json().catch(() => ({}));
      return NextResponse.json({
        message: generateFallbackResponse(body.messages || [], body.mode),
        fallback: true,
      });
    } catch {
      return NextResponse.json({
        message: 'متأسفانه خطایی رخ داد. لطفاً دوباره تلاش کنید.',
        error: true,
      });
    }
  }
}

