// ── POST handler ─────────────────────────────────────────────────────────────

export async function POST() {
  try {
    // IDEMPOTENT CHECK
    const contractCount = await db.contract.count();
    if (contractCount > 0) {
      return NextResponse.json({
        message: 'Contracts already seeded',
        seeded: false,
        existing: contractCount,
      });
    }

    const categoryCount = await db.contractCategory.count();
    if (categoryCount > 0) {
      return NextResponse.json({
        message: 'Contract categories already seeded',
        seeded: false,
        existing: categoryCount,
      });
    }

    // Speed up SQLite
    try {
      await db.$executeRaw('PRAGMA synchronous = OFF');
    } catch (e: unknown) {
      console.log('PRAGMA sync off skipped:', (e as Error).message);
    }
    try {
      await db.$executeRaw('PRAGMA journal_mode = WAL');
    } catch (e: unknown) {
      console.log('PRAGMA WAL skipped:', (e as Error).message);
    }

    // ============ CREATE CATEGORIES ============
    console.log('Creating contract categories...');
    await db.contractCategory.createMany({
      data: CATEGORIES.map((cat) => ({
        name: cat.name,
        slug: cat.slug,
        icon: cat.icon,
        color: cat.color,
        order: cat.order,
      })),
    });

    // Build slug → id map
    const allCategories = await db.contractCategory.findMany();
    const slugToId = new Map<string, string>();
    for (const cat of allCategories) {
      slugToId.set(cat.slug, cat.id);
    }

    // Fallback: if CONTRACTS_DATA references a categorySlug that doesn't
    // exist in our 15 flat categories, route it to 'other'
    const defaultCategoryId = slugToId.get('other')!;

    const resolveCategoryId = (categorySlug: string): string =>
      slugToId.get(categorySlug) || defaultCategoryId;

    // ============ PREPARE CONTRACT RECORDS ============
    console.log(`Preparing ${CONTRACTS_DATA.length} contract records...`);

    const contractRecords = CONTRACTS_DATA.map((item) => {
      const categoryId = resolveCategoryId(item.categorySlug);

      // Use the pre-built content from CONTRACTS_DATA if available,
      // otherwise generate one via our helper
      const content = item.content
        ? item.content
        : generateContractContent(
            item.title,
            item.categorySlug,
            item.tags || [],
            item.applicableLaws || [],
            item.difficulty || 'GENERAL',
          );

      return {
        title: item.title,
        slug: item.slug,
        description: item.description,
        content,
        summary: item.summary,
        categoryId,
        tags: item.tags ? JSON.stringify(item.tags) : null,
        applicableLaws: item.applicableLaws
          ? JSON.stringify(item.applicableLaws)
          : null,
        difficulty: item.difficulty || 'GENERAL',
        isPublished: true,
        version: '1.0',
      };
    });

    // ============ INSERT CONTRACTS IN BATCHES OF 50 ============
    const BATCH_SIZE = 50;
    let totalCreated = 0;

    for (let i = 0; i < contractRecords.length; i += BATCH_SIZE) {
      const batch = contractRecords.slice(i, i + BATCH_SIZE);
      const result = await db.contract.createMany({ data: batch });
      totalCreated += result.count;
      console.log(
        `Inserted batch ${Math.floor(i / BATCH_SIZE) + 1}: ${result.count} contracts (${i + BATCH_SIZE > contractRecords.length ? contractRecords.length : i + BATCH_SIZE}/${contractRecords.length})`,
      );
    }

    // ============ RETURN RESULT ============
    return NextResponse.json({
      message: 'Contracts seeded successfully',
      seeded: true,
      categoriesCreated: CATEGORIES.length,
      contractsCreated: totalCreated,
    });
  } catch (error) {
    console.error('Error seeding contracts:', error);
    return NextResponse.json(
      {
        message: 'Error seeding contracts',
        error: error instanceof Error ? error.message : String(error),
      },
      { status: 500 },
    );
  }
}
